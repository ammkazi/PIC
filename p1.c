#include <stdio.h>

int main()
{
    printf("Hello World\n");
    return 0;
}

/*
1. #include - preprocessor directive
2. <stdio.h> - standard i/o header file
3. main - function - it is also the entry point of the program
4. int - Written before main - I will return an integer value
5. {} - Braces - we write block of code inside the braces
6. printf() - called function - helps to print the output
7. \n = escape sequence - takes the cursor to the new line
8. We write statements inside the function
9. ; (semi colon) is called is a statement terminator
10. we compile the code using gcc compiler - (gcc p1.c)
*/